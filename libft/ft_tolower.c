#include "libft.h"

/*
** [PROTOTYPE]
** #include <ctype.h>
** int	ft_tolower(int c);
*/

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	else
		return (c);
}
